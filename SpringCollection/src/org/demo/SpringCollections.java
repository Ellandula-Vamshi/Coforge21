package org.demo;

import java.util.List;
import java.util.Map;
import java.util.Properties;
import java.util.Set;

public class SpringCollections {

	private List fruits;
	private Set  animals;
	private Map  empDetails;
	private Properties integers;
	public List getFruits() {
		return fruits;
	}
	public void setFruits(List fruits) {
		this.fruits = fruits;
	}
	public Set getAnimals() {
		return animals;
	}
	public void setAnimals(Set animals) {
		this.animals = animals;
	}
	public Map getEmpDetails() {
		return empDetails;
	}
	public void setEmpDetails(Map empDetails) {
		this.empDetails = empDetails;
	}
	public Properties getIntegers() {
		return integers;
	}
	public void setIntegers(Properties integers) {
		this.integers = integers;
	}
	
	
	
	
	
}
